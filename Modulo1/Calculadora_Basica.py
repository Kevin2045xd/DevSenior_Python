print(f"la division de los numeros es:{(numero1/numero2)}")
print(f"la division entera de los numeros es:{numero1//numero2}")
print(f"el modulo de los numeros es:{numero1%numero2}")
print(f"la potencia de los numeros es:{numero1**numero2}")
